    /**
     * Display the specified resource.
     *
     * @param  \App\provider  $provider
     * @return \Illuminate\Http\Response
     */
    public function show(provider $provider,$id,$name)
    {

        //print_r($provider);exit;
        $product = $provider->where(['provider_name'=>$id,"product_name"=>$name])->get();
 
        if ($product->isEmpty()) {
            return response()->json([
                'success' => false,
                'message' => 'product name with  ' . $name . ' not found'." in provider ".$id
            ], 400);
        }
 
        return response()->json([
            'success' => true,
            'data' => $product->toArray()
        ], 400);
    }

